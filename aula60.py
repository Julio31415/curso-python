"""
Introdução as funções (def) em Python
Funções são trechos de codigo usados para replicar determinada ação ao longo do seu codígo.
Elas podem receber valores para parâmetros (argumentos) e retornar um valor especifico
Por padrão, funções Python retornam None (nada)
"""
def Print(a,b,c):
    print(a)
    print(b)
    print(c)
Print(1,2,3)